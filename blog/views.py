# Helper functions
def get_date(post):
    return post["date"]

# Create your views here.

def starting_page(request):
    # sorted(list,key) --> it takes a list and a key(comparision-factor) & returns a new sorted list
    # list.sort(key) --> it INLINE sorts the list based on the key, thus no need of second variable
    sorted_posts = sorted(posts_data, key=get_date) # the sorting fun automatically calls the get_date fun
    latest_posts = sorted_posts[-3:]
    return render(request,"blog/index.html", {
        "posts": latest_posts,
    })

def posts(request):
    sorted_posts = sorted(posts_data, key=get_date)
    return render(request, "blog/all-posts.html",{
        "all_posts": sorted_posts,
    })

def post_detail(request, slug):
    # next() --> is a list iterator that finds the very next matching element
    id_post = next(post for post in posts_data if post['slug'] == slug)
    return render(request, "blog/post-detail.html", {
        "post": id_post,
    })